        // Hero section slider functionality
        document.addEventListener('DOMContentLoaded', function() {
    const slides = [
        { url: 'assets/images/cancer1.jpg', title: 'Supporting Your Cancer Journey' },
        { url: 'assets/images/cancer2.jpg', title: 'Together We Are Stronger' },
        { url: 'assets/images/cancer3.jpg', title: 'Hope, Healing, and Community' },
        { url: 'assets/images/cancer4.jpg', title: 'Comprehensive Support Services' },
        { url: 'assets/images/cancer5.jpg', title: 'Empowering Cancer Survivors' }
    ];

    const heroSection = document.querySelector('.hero');
    
    // Create slides
    slides.forEach((slide, index) => {
        const slideDiv = document.createElement('div');
        slideDiv.className = `hero-slide ${index === 0 ? 'active' : ''}`;
        slideDiv.style.backgroundImage = `url(${slide.url})`;
        heroSection.appendChild(slideDiv);
    });

    let currentSlide = 0;
    const totalSlides = slides.length;

    function nextSlide() {
        const activeSlide = document.querySelector('.hero-slide.active');
        activeSlide.classList.remove('active');
        
        currentSlide = (currentSlide + 1) % totalSlides;
        
        const slides = document.querySelectorAll('.hero-slide');
        slides[currentSlide].classList.add('active');
        
        // Update dots
        updateDots();
    }

    // Create dot controls
    const sliderControls = document.querySelector('.slider-controls');
    slides.forEach((_, index) => {
        const dot = document.createElement('div');
        dot.className = `slider-dot ${index === 0 ? 'active' : ''}`;
        dot.addEventListener('click', () => goToSlide(index));
        sliderControls.appendChild(dot);
    });

    function updateDots() {
        const dots = document.querySelectorAll('.slider-dot');
        dots.forEach((dot, index) => {
            dot.classList.toggle('active', index === currentSlide);
        });
    }

    function goToSlide(index) {
        const activeSlide = document.querySelector('.hero-slide.active');
        activeSlide.classList.remove('active');
        
        currentSlide = index;
        
        const slides = document.querySelectorAll('.hero-slide');
        slides[currentSlide].classList.add('active');
        
        updateDots();
    }

    // Auto-advance slides
    setInterval(nextSlide, 5000);
});
    </script>
